package sample.Structs;

public class OutdoorGameHints {
    int idHints;
    int idOutdoorGame;
    int idPoint;
    String hint;

    public int getIdHints() {
        return idHints;
    }

    public void setIdHints(int idHints) {
        this.idHints = idHints;
    }

    public int getIdOutdoorGame() {
        return idOutdoorGame;
    }

    public void setIdOutdoorGame(int idOutdoorGame) {
        this.idOutdoorGame = idOutdoorGame;
    }

    public int getIdPoint() {
        return idPoint;
    }

    public void setIdPoint(int idPoint) {
        this.idPoint = idPoint;
    }

    public String getHint() {
        return hint;
    }

    public void setHint(String hint) {
        this.hint = hint;
    }
}
